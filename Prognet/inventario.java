    void modificar(){
        int fila = jTable1.getSelectedRow();
    if (fila == -1) {
        JOptionPane.showMessageDialog(null, "Debe seleccionar un producto");
        return;
    }
    
    int n = Integer.parseInt(jTable1.getValueAt(fila, 0).toString());
    String art = txtarticulo.getText();
    String mar = txtmarca.getText();
    String mod = txtmodelo.getText();
    String cod = txtcodigo.getText();
    String fec = txtfecha.getText(); // La fecha debe estar en formato "AAAA-MM-DD"
    String cost = txtcosto.getText();
    String sql = "UPDATE inventario SET articulo=?, marca=?, modelo=?, codigo=?, fecha=?, costo=? WHERE n=?";

    if (art.equals("") || mar.equals("") || mod.equals("") || cod.equals("") || fec.equals("")) {
        JOptionPane.showMessageDialog(null, "Debe ingresar datos");
    } else {
        try {
            Connection conexion = Conexion.getConnection();
            PreparedStatement consulta = conexion.prepareStatement(sql);
            consulta.setString(1, art);
            consulta.setString(2, mar);
            consulta.setString(3, mod);
            consulta.setString(4, cod);
            consulta.setString(5, fec);
            consulta.setString(6, cost);
            consulta.setInt(7, n);
            consulta.executeUpdate();
            JOptionPane.showMessageDialog(null, "Producto actualizado correctamente");
            
            // Actualizar la tabla
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setValueAt(n, fila, 0);
            modelo.setValueAt(art, fila, 1);
            modelo.setValueAt(mar, fila, 2);
            modelo.setValueAt(mod, fila, 3);
            modelo.setValueAt(cod, fila, 4);
            modelo.setValueAt(fec, fila, 5);
            modelo.setValueAt(cost, fila, 6);
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al actualizar el producto: " + e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }
}
    
    
