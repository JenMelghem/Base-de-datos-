        void cargarTabla() {
   try {
            Connection conexion = Conexion.getConnection();
            String sql = "SELECT * FROM inventario";
            PreparedStatement consulta = conexion.prepareStatement(sql);
            ResultSet resultado = consulta.executeQuery();
            
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("N");
            modelo.addColumn("Articulo");
            modelo.addColumn("Marca");
            modelo.addColumn("Modelo");
            modelo.addColumn("Codigo");
            modelo.addColumn("Fecha");
            modelo.addColumn("Costo");
            
            while (resultado.next()) {
                Object[] fila = new Object[7];
                fila[0] = resultado.getInt("N");
                fila[1] = resultado.getString("Articulo");
                fila[2] = resultado.getString("Marca");
                fila[3] = resultado.getString("Modelo");
                fila[4] = resultado.getInt("codigo");
                fila[5] = resultado.getDate("fecha");
                fila[6] = resultado.getFloat("costo");
                modelo.addRow(fila);
            }
            
            jTable1.setModel(modelo);
            
            resultado.close();
            consulta.close();
            conexion.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar la tabla: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }  

